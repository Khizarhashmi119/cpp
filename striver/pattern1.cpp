#include <iostream>
#include <bits/stdc++.h>

using namespace std;

int main()
{
  int noOfRowsAndColumns = 10;

  for (int i = 0; i < noOfRowsAndColumns; i++)
  {
    for (int j = 0; j < noOfRowsAndColumns; j++)
      cout << "*";

    cout << endl;
  }

  return 0;
}

